package com.jm.ppl.admin.likecount.vo;

public class DramaVO {

	

	private String dramaId;
	private String dramaTitle;
	private String dramaProducer;
	private String dramaWriter;
	private String dramaScreeningTime;
	private String dramaGenre;
	private String dramaActor;
	private String dramaPost;
	private int dramaLikeCount;
	
	
	public String getDramaId() {
		return dramaId;
	}
	public void setDramaId(String dramaId) {
		this.dramaId = dramaId;
	}
	public String getDramaTitle() {
		return dramaTitle;
	}
	public void setDramaTitle(String dramaTitle) {
		this.dramaTitle = dramaTitle;
	}
	public String getDramaProducer() {
		return dramaProducer;
	}
	public void setDramaProducer(String dramaProducer) {
		this.dramaProducer = dramaProducer;
	}
	public String getDramaWriter() {
		return dramaWriter;
	}
	public void setDramaWriter(String dramaWriter) {
		this.dramaWriter = dramaWriter;
	}
	public String getDramaScreeningTime() {
		return dramaScreeningTime;
	}
	public void setDramaScreeningTime(String dramaScreeningTime) {
		this.dramaScreeningTime = dramaScreeningTime;
	}
	public String getDramaGenre() {
		return dramaGenre;
	}
	public void setDramaGenre(String dramaGenre) {
		this.dramaGenre = dramaGenre;
	}
	public String getDramaActor() {
		return dramaActor;
	}
	public void setDramaActor(String dramaActor) {
		this.dramaActor = dramaActor;
	}
	public String getDramaPost() {
		return dramaPost;
	}
	public void setDramaPost(String dramaPost) {
		this.dramaPost = dramaPost;
	}
	public int getDramaLikeCount() {
		return dramaLikeCount;
	}
	public void setDramaLikeCount(int likeCount) {
		this.dramaLikeCount = likeCount;
	}
	
}
