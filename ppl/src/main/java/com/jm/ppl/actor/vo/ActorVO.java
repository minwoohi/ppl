package com.jm.ppl.actor.vo;

public class ActorVO {

	private String actorId;
	private String actorName;
	private String actorSex;
	private String actorBirth;
	private String actorHeight;
	private String actorWeight;
	private String actorEntertainment;
	private int actorLikeCount;
	private String actorPost;
	
	public String getActorId() {
		return actorId;
	}
	public void setActorId(String actorId) {
		this.actorId = actorId;
	}
	public String getActorName() {
		return actorName;
	}
	public void setActorName(String actorName) {
		this.actorName = actorName;
	}
	public String getActorSex() {
		return actorSex;
	}
	public void setActorSex(String actorSex) {
		this.actorSex = actorSex;
	}
	public String getActorBirth() {
		return actorBirth;
	}
	public void setActorBirth(String actorBirth) {
		this.actorBirth = actorBirth;
	}
	public String getActorHeight() {
		return actorHeight;
	}
	public void setActorHeight(String actorHeight) {
		this.actorHeight = actorHeight;
	}
	public String getActorWeight() {
		return actorWeight;
	}
	public void setActorWeight(String actorWeight) {
		this.actorWeight = actorWeight;
	}
	public String getActorEntertainment() {
		return actorEntertainment;
	}
	public void setActorEntertainment(String actorEntertainment) {
		this.actorEntertainment = actorEntertainment;
	}
	public int getActorLikeCount() {
		return actorLikeCount;
	}
	public void setActorLikeCount(int actorLikeCount) {
		this.actorLikeCount = actorLikeCount;
	}
	public String getActorPost() {
		return actorPost;
	}
	public void setActorPost(String actorPost) {
		this.actorPost = actorPost;
	}
	
	
}
